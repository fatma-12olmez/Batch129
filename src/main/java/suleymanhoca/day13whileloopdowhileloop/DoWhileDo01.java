package suleymanhoca.day13whileloopdowhileloop;

public class DoWhileDo01 {
    public static void main(String[] args) {
        //1)while-loop
        int i=1;
        while (i<1){
            System.out.println("Sen bir while loop's'un");
            i++;


        }//While body hic calismadi=============zero execution==========While loop'ta Zero Execution mumkundur.





        //1)Do-while-loop
        int k=1;

        do {

            System.out.println("Sen bir do while loop'sun");
            k++;
           }while(k<1);

        /*
        1)Do while body calisti
        2)do while kullandiginizda loop body en az bir kere calisir.
        do while'da Zero execution mumkun degildir.
         */


    }
}
